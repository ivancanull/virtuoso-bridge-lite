class RAMICBridge(VirtuosoInterface):
    """Virtuoso SKILL bridge using the RAMIC daemon over TCP."""

    def __init__(
        self,
        remote: bool = False,
        host: str | None = None,
        port: int | None = None,
        timeout: int = 30,
        remote_host: str | None = None,
        remote_user: str | None = None,
        remote_work_dir: str | None = None,
        jump_host: str | None = None,
        jump_user: str | None = None,
        auto_setup: bool = True,
        keep_remote_files: bool = False,
        log_to_ciw: bool = True,
    ) -> None:
        self._remote_mode = remote

        if remote:
            self._host = "127.0.0.1"
            self._remote_host = _bridge_env("VB_REMOTE_HOST") or None
            self._remote_user = _bridge_env("VB_REMOTE_USER") or None
            try:
                self._port = int(os.getenv("VB_REMOTE_PORT", "").strip() or _default_remote_port(self._remote_user))
            except (ValueError, TypeError, AttributeError):
                self._port = _default_remote_port(self._remote_user)
            self._jump_host = _bridge_env("VB_JUMP_HOST") or None
            self._jump_user = _bridge_env("VB_JUMP_USER") or None
            self._remote_work_dir_user_provided = False
            self._remote_work_dir = None
        else:
            self._host = host or "127.0.0.1"
            try:
                self._port = port if port is not None else int(os.getenv("VB_REMOTE_PORT", "").strip() or _default_remote_port())
            except (ValueError, TypeError):
                self._port = _default_remote_port()
            self._remote_host = remote_host
            self._remote_user = remote_user
            self._remote_work_dir_user_provided = remote_work_dir is not None
            self._remote_work_dir = remote_work_dir
            self._jump_host = jump_host
            self._jump_user = jump_user

        self._timeout = timeout
        self._auto_setup = auto_setup
        self._keep_remote_files = keep_remote_files
        self._log_to_ciw = log_to_ciw
        self.layout = LayoutOps(self)
        self.schematic = SchematicOps(self)

        self._remote_setup_done = False
        self._remote_virtuoso_setup_path: str | None = None
        self._ssh_tunnel_proc: subprocess.Popen[bytes] | None = None
        self._using_external_tunnel = False
        self._ssh_runner: SSHRunner | None = None
        self._il_upload_cache: dict[str, tuple[str, str]] = {}  # path -> (md5, remote_path)

        if self._remote_host:
            self._ssh_runner = SSHRunner(
                host=self._remote_host,
                user=self._remote_user,
                jump_host=self._jump_host,
                jump_user=self._jump_user,
                persistent_shell=True,
                verbose=True,
            )

    # -- factory methods ----------------------------------------------------

    @classmethod
    def from_env(
        cls,
        *,
        timeout: int = 30,
        auto_setup: bool = True,
        keep_remote_files: bool = False,
        log_to_ciw: bool = True,
    ) -> "RAMICBridge":
        """Create a remote bridge from environment variables / .env file."""
        load_dotenv()
        return cls(
            remote=True,
            timeout=timeout,
            auto_setup=auto_setup,
            keep_remote_files=keep_remote_files,
            log_to_ciw=log_to_ciw,
        )

    @classmethod
    def local(
        cls,
        host: str = "127.0.0.1",
        port: int = 65432,
        timeout: int = 30,
    ) -> "RAMICBridge":
        """Create a bridge for a locally running daemon."""
        return cls(remote=False, host=host, port=port, timeout=timeout)

    # -- context manager ----------------------------------------------------

    def __enter__(self) -> "RAMICBridge":
        return self

    def __exit__(self, *_: Any) -> None:
        self.close()

