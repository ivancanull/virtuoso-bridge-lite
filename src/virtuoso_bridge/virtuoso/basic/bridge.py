    def ciw_print(self, message: str, timeout: int | None = None) -> VirtuosoResult:
        """Print a message to the Virtuoso CIW."""
        effective_timeout = timeout if timeout is not None else self._timeout
        escaped = _escape_skill_string(message)
        return self.execute_skill(
            f'progn(printf("%s\\n" "{escaped}") t)',
            timeout=effective_timeout,
        )

    def ciw_log(self, skill_code: str, timeout: int | None = None) -> VirtuosoResult:
        """Execute SKILL code and print command + result to CIW."""
        effective_timeout = timeout if timeout is not None else self._timeout
        code = skill_code.strip()
        if not self._log_to_ciw:
            return self.execute_skill(code, timeout=effective_timeout)
        esc_eval = _escape_for_skill_evalstring_source(code)
        esc_display = _escape_skill_string(code)
        wrapped = (
            f"prog((rbRret rbMsg) "
            f'printf("%s\\n" strcat("RAMIC >> " "{esc_display}")) '
            f'rbRret = evalstring("{esc_eval}") '
            f'rbMsg = if(stringp(rbRret) then rbRret else sprintf(nil "%L" rbRret)) '
            f'printf("%s\\n" strcat("RAMIC << " rbMsg)) '
            f"return(rbRret))"
        )
        return self.execute_skill(wrapped, timeout=effective_timeout)

