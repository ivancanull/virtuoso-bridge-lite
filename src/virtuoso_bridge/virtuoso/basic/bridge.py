    def ensure_ready(self, timeout: int = 10) -> VirtuosoResult:
        """Ensure the bridge is ready: remote setup, tunnel, and daemon reachable."""
        metadata: dict[str, Any] = {}
        errors: list[str] = []

        if self._remote_mode and not (self._remote_host and self._remote_host.strip()):
            return VirtuosoResult(
                status=ExecutionStatus.ERROR,
                errors=["VB_REMOTE_HOST must be set and non-empty when remote=True"],
                metadata={"remote": True},
            )

        if self._remote_host and self._auto_setup and not self._remote_setup_done:
            try:
                self.ensure_remote_setup()
                metadata["remote_setup_done"] = True
            except Exception as e:  # noqa: BLE001
                logger.warning("Remote setup failed: %s", e)
                errors.append(f"Remote setup failed: {e}")
                metadata["remote_setup_error"] = str(e)
                return VirtuosoResult(
                    status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
                )

        if self._remote_host:
            try:
                self.ensure_ssh_tunnel()
                metadata["tunnel_alive"] = self.is_tunnel_alive
            except Exception as e:  # noqa: BLE001
                logger.warning("SSH tunnel failed: %s", e)
                errors.append(f"SSH tunnel failed: {e}")
                metadata["tunnel_error"] = str(e)
                metadata["tunnel_alive"] = False
                return VirtuosoResult(
                    status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
                )

        report = self.verify_tunnel(timeout=timeout)
        metadata["diagnostics"] = report
        if not report["tcp_reachable"]:
            errors.append("TCP connection to daemon host:port failed")
        if not report["daemon_responsive"]:
            errors.append("Daemon did not respond to ping (ensure load() in CIW)")

        if errors:
            if self._remote_virtuoso_setup_path and not report.get("daemon_responsive", False):
                print(
                    "\nPlease execute in Virtuoso CIW: "
                    f'load("{self._remote_virtuoso_setup_path}")\n'
                )
            return VirtuosoResult(
                status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
            )
        return VirtuosoResult(status=ExecutionStatus.SUCCESS, metadata=metadata)

    def warm_remote_session(self, timeout: int = 10) -> VirtuosoResult:
        """Warm up remote transports without requiring the daemon to answer."""
        metadata: dict[str, Any] = {}
        errors: list[str] = []

        if self._remote_mode and not (self._remote_host and self._remote_host.strip()):
            return VirtuosoResult(
                status=ExecutionStatus.ERROR,
                errors=["VB_REMOTE_HOST must be set and non-empty when remote=True"],
                metadata={"remote": True},
            )

        if self._remote_host and self._auto_setup and not self._remote_setup_done:
            try:
                self.ensure_remote_setup()
                metadata["remote_setup_done"] = True
            except Exception as e:  # noqa: BLE001
                errors.append(f"Remote setup failed: {e}")
                metadata["remote_setup_error"] = str(e)
                return VirtuosoResult(
                    status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
                )

        if self._ssh_runner is not None and self._ssh_runner.persistent_shell_enabled:
            try:
                self._ssh_runner.ensure_persistent_shell(timeout=timeout)
                metadata["persistent_shell_ready"] = True
            except Exception as e:  # noqa: BLE001
                errors.append(f"Persistent SSH shell failed: {e}")
                metadata["persistent_shell_error"] = str(e)
                return VirtuosoResult(
                    status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
                )

        if self._remote_host:
            try:
                self.ensure_ssh_tunnel()
                metadata["tunnel_alive"] = self.is_tunnel_alive
            except Exception as e:  # noqa: BLE001
                errors.append(f"SSH tunnel failed: {e}")
                metadata["tunnel_error"] = str(e)
                metadata["tunnel_alive"] = False
                return VirtuosoResult(
                    status=ExecutionStatus.ERROR, errors=errors, metadata=metadata
                )

        return VirtuosoResult(status=ExecutionStatus.SUCCESS, metadata=metadata)

    def load_il(self, path: str | Path, timeout: int | None = None) -> VirtuosoResult:
        """Load an IL file in Virtuoso."""
        try:
            prepared, uploaded = self._prepare_il_path(path)
        except Exception as e:  # noqa: BLE001
            logger.warning("Path preparation failed for %s: %s", path, e)
            return VirtuosoResult(
                status=ExecutionStatus.ERROR,
                errors=[f"Path preparation failed: {e}"],
                metadata={"path": str(path)},
            )
        effective_timeout = timeout if timeout is not None else self._timeout
        pe = _escape_skill_string(prepared)
        if self._log_to_ciw:
            skill_code = (
                f"prog((rbLret rbMsg) "
                f'printf("%s\\n" strcat("RAMIC load_il >> " "{pe}")) '
                f'rbLret = load("{pe}") '
                f'rbMsg = if(stringp(rbLret) then rbLret else sprintf(nil "%L" rbLret)) '
                f'printf("%s\\n" strcat("RAMIC load_il << " rbMsg)) '
                f"return(rbLret))"
            )
        else:
            skill_code = f'load("{pe}")'
        result = self.execute_skill(skill_code, timeout=effective_timeout)
        result.metadata["skill_command"] = f'load("{prepared}")'
        result.metadata["uploaded"] = uploaded
        return result

    def open_cell_view(
        self,
        lib: str,
        cell: str,
        *,
        view: str = "layout",
        view_type: str | None = None,
        mode: str = "w",
        timeout: int | None = None,
    ) -> VirtuosoResult:
        """Open a target cellview and bind it to ``cv`` in Virtuoso."""
        effective_timeout = timeout if timeout is not None else self._timeout
        resolved_view_type = view_type or default_view_type_for(view)
        skill_code = op_open_cell_view(
            lib,
            cell,
            view=view,
            view_type=resolved_view_type,
            mode=mode,
        )
        result = self.execute_skill(skill_code, timeout=effective_timeout)
        if result.status != ExecutionStatus.SUCCESS:
            return result
        if (result.output or "").strip().lower() == "nil":
            return VirtuosoResult(
                status=ExecutionStatus.ERROR,
                errors=[f"Failed to open cellview {lib}/{cell}/{view}"],
                metadata={
                    "lib": lib,
                    "cell": cell,
                    "view": view,
                    "view_type": resolved_view_type,
                    "mode": mode,
                },
                execution_time=result.execution_time,
            )
        return result

